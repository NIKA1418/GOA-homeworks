# 5. მომხმარებლისგან რიცხვების შემოტანა და შედარება
num1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = int(input("შეიყვანეთ მეორე რიცხვი: "))

print("პირველი რიცხვი > მეორე რიცხვი:", num1 > num2)
print("პირველი რიცხვი < მეორე რიცხვი:", num1 < num2)
print("პირველი რიცხვი == მეორე რიცხვი:", num1 == num2)
print("პირველი რიცხვი != მეორე რიცხვი:", num1 != num2)